package com.openclassrooms.safetyNet.model;

public class PhoneAlert {
private  String phone;

    public PhoneAlert() {
        this.phone = phone;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    @Override
    public String toString() {
        return "PhoneAlert{" +
                "phone='" + phone + '\'' +
                '}';
    }
}
